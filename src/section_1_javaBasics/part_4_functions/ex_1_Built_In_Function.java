package section_1_javaBasics.part_4_functions;
/* Built-in function
- pre-defined functions that come with the Java standard library.
- sqrt() is a method of Math class. It returns the square root of a number.
- The main advantage is code reusability. We can write a method once, and use it multiple times.
- We do not have to rewrite the entire code each time.
- Think of it as, "write once, reuse multiple times".
 */
public class ex_1_Built_In_Function {
    public static void main(String[] args) {
        // Math class Methods
        System.out.println("Square root of 4 is : "+ Math.sqrt(4));
    }

}
